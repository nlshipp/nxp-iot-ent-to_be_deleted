static void write_reg(uint8_t value, uint32_t offset)
{
	*(volatile uint8_t *)((uint64_t)(DW_HDMI_PHY_BASE + offset)) = value;
}

EFI_STATUS dw_hdmi_imx_phy_clk_set_pll(uint32_t rate, uint32_t *real_rate)
{
	const struct phy_cfg *phy_cfg = phy_pll_cfg;
	const struct phy_cfg *best_phy_cfg = NULL;
	uint32_t best_diff = 297000000, diff;
	int i;

	for (; phy_cfg->rate != 0; phy_cfg++) {
		if (phy_cfg->rate > rate) {
			diff = phy_cfg->rate - rate;
		} else {
			diff = rate - phy_cfg->rate;
		}
		if (diff < best_diff) {
			best_diff = diff;
			best_phy_cfg = phy_cfg;
		}
	}

	if (best_phy_cfg == NULL) {
		DEBUG ((DEBUG_ERROR, "dw_hdmi_imx_phy_clk_set_pll: ERROR PLL rate not found for pixel clock: %d Hz\n", rate));
		*real_rate = 0;
		return EFI_INVALID_PARAMETER;
	}
	if (best_phy_cfg->rate != rate) {
		DEBUG ((DEBUG_ERROR, "dw_hdmi_imx_phy_clk_set_pll: WARNING PPL rate not found for pixel clock: %d. Adjusting to %d instead.\n",
			rate, best_phy_cfg->rate));
	} else {
		DEBUG ((DEBUG_ERROR, "dw_hdmi_imx_phy_clk_set_pll: PLL rate found for pixel clock %d\n", best_phy_cfg->rate));
	}

	/* HDMI PHY init */
	write_reg(FIX_DA, PHY_REG_84);

	for (i = 0; i <  PHY_REGS_NUM; i++) {
		write_reg(best_phy_cfg->regs[i], i * 4);
	}

	write_reg(FIX_DA | MODE_SET_DONE , PHY_REG_84);

	/* Wait for PHY PLL lock */
	MicroSecondDelay(20);
	*real_rate = best_phy_cfg->rate;

	return EFI_SUCCESS;
}

